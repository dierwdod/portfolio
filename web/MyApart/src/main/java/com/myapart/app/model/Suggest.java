package com.myapart.app.model;

public class Suggest {
	private int sugNum;
	private String sugTitle;
	private String sugContents;
	private String sugDate;
	private int sugCount;
	private String id;
	public int getSugNum() {
		return sugNum;
	}
	public void setSugNum(int sugNum) {
		this.sugNum = sugNum;
	}
	public String getSugTitle() {
		return sugTitle;
	}
	public void setSugTitle(String sugTitle) {
		this.sugTitle = sugTitle;
	}
	public String getSugContents() {
		return sugContents;
	}
	public void setSugContents(String sugContents) {
		this.sugContents = sugContents;
	}
	public String getSugDate() {
		return sugDate;
	}
	public void setSugDate(String sugDate) {
		this.sugDate = sugDate;
	}
	public int getSugCount() {
		return sugCount;
	}
	public void setSugCount(int sugCount) {
		this.sugCount = sugCount;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
}
