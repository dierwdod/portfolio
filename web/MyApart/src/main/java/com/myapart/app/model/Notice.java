package com.myapart.app.model;

public class Notice {
	private int notNum;
	private String notTitle;
	private String notContents;
	private String notDate;
	private int notCount;
	private String id;

	public int getNotNum() {
		return notNum;
	}
	public void setNotNum(int notNum) {
		this.notNum = notNum;
	}
	public String getNotTitle() {
		return notTitle;
	}
	public void setNotTitle(String notTitle) {
		this.notTitle = notTitle;
	}
	public String getNotContents() {
		return notContents;
	}
	public void setNotContents(String notContents) {
		this.notContents = notContents;
	}
	public String getNotDate() {
		return notDate;
	}
	public void setNotDate(String notDate) {
		this.notDate = notDate;
	}
	public int getNotCount() {
		return notCount;
	}
	public void setNotCount(int notCount) {
		this.notCount = notCount;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
}
